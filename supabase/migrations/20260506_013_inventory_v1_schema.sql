-- Lane INVENTORY-V1 (2026-05-06 evening) — additive schema for Premium-tier
-- inventory feature. Default OFF; opt-in per tenant via
-- tenants.feature_flags->>'inventory_enabled' = 'true'.
--
-- Tables (all RLS-enabled, all grant-locked):
--   1. inventory_items       — raw stock units per tenant (kg of flour, etc.)
--   2. menu_item_recipes     — many-to-many: which inventory_items are
--                              consumed when a menu_item is sold, qty per
--                              serving. Trigger (PR 3a) iterates this on
--                              order DELIVERED to deplete stock.
--   3. suppliers             — supplier directory (per tenant)
--   4. purchase_orders       — DRAFT generated by daily reorder cron (PR 3b);
--                              owner reviews then "sends" (PR 3c, deferred).
--   5. inventory_movements   — append-only ledger of every stock change.
--
-- Plus:
--   - tenants.feature_flags JSONB column (default '{}'), so this PR can ship
--     before the trigger PR and the trigger can no-op when the flag is false.
--
-- Distribution impact: Premium-tier upsell + AI-data-richness foundation.
-- Hepy orchestrator gains inventory awareness for forecasting / reorder /
-- waste detection. Brașov pilot opt-in, not required.
--
-- Idempotent: every CREATE/ALTER uses IF NOT EXISTS / IF EXISTS. Re-running
-- the migration is a no-op.

-- ============================================================
-- 0. tenants.feature_flags
-- ============================================================
alter table public.tenants
  add column if not exists feature_flags jsonb not null default '{}'::jsonb;

comment on column public.tenants.feature_flags is
  'Per-tenant feature gating. Keys are kebab-case-or-snake_case feature ids; values are boolean (or string for tiered features). Read in app via supabase select; write via OWNER admin UI. First key: inventory_enabled (Premium tier).';

-- ============================================================
-- 1. inventory_items
-- ============================================================
create table if not exists public.inventory_items (
  id                  uuid primary key default gen_random_uuid(),
  tenant_id           uuid not null references public.tenants(id) on delete cascade,
  name                text not null,
  unit                text not null
    check (unit in ('kg','g','l','ml','buc','portie')),
  current_stock       numeric(12,3) not null default 0,
  reorder_threshold   numeric(12,3) not null default 0,
  reorder_quantity    numeric(12,3) not null default 0,
  supplier_id         uuid,  -- FK added below after suppliers table created
  notes               text,
  created_at          timestamptz not null default now(),
  updated_at          timestamptz not null default now()
);

create index if not exists idx_inventory_items_tenant
  on public.inventory_items(tenant_id);

create index if not exists idx_inventory_items_low_stock
  on public.inventory_items(tenant_id)
  where current_stock <= reorder_threshold;

alter table public.inventory_items enable row level security;

drop policy if exists inventory_items_member_read on public.inventory_items;
create policy inventory_items_member_read
  on public.inventory_items for select to authenticated
  using (
    exists (select 1 from public.tenant_members tm
             where tm.tenant_id = inventory_items.tenant_id
               and tm.user_id = auth.uid())
  );

-- writes go via service-role only (server actions in admin app)
revoke insert, update, delete, truncate, references, trigger
  on table public.inventory_items from anon;
revoke insert, update, delete, truncate, references, trigger
  on table public.inventory_items from authenticated;
revoke select on table public.inventory_items from anon;

comment on table public.inventory_items is
  'Raw stock units per tenant (Premium feature). Depleted by trigger on order DELIVERED via menu_item_recipes. RLS: tenant members read; service-role writes.';

