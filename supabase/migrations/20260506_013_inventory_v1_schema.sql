-- ============================================================
-- 6. updated_at triggers (reuse existing helper)
-- ============================================================
-- Reuse the canonical public.touch_updated_at() helper (already used by
-- restaurant_orders et al). Wrapped in DO block so the migration is still
-- a soft no-op if a future refactor renames the helper.
do $$
begin
  if exists (select 1 from pg_proc p join pg_namespace n on p.pronamespace = n.oid
              where n.nspname = 'public' and p.proname = 'touch_updated_at') then
    if not exists (select 1 from pg_trigger where tgname = 'trg_inventory_items_updated_at') then
      execute 'create trigger trg_inventory_items_updated_at before update on public.inventory_items for each row execute function public.touch_updated_at()';
    end if;
    if not exists (select 1 from pg_trigger where tgname = 'trg_suppliers_updated_at') then
      execute 'create trigger trg_suppliers_updated_at before update on public.suppliers for each row execute function public.touch_updated_at()';
    end if;
    if not exists (select 1 from pg_trigger where tgname = 'trg_menu_item_recipes_updated_at') then
      execute 'create trigger trg_menu_item_recipes_updated_at before update on public.menu_item_recipes for each row execute function public.touch_updated_at()';
    end if;
    if not exists (select 1 from pg_trigger where tgname = 'trg_purchase_orders_updated_at') then
      execute 'create trigger trg_purchase_orders_updated_at before update on public.purchase_orders for each row execute function public.touch_updated_at()';
    end if;
  end if;
end $$;
